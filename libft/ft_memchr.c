#include "libft.h"

void	*ft_memchr(const void *s, int c, size_t n)
{
	const char	*str;
	size_t		i;

	i = 0;
	str = s;
	while (i < n)
	{
		if (str[i] == (char)c)
			return ((void *)s + i);
		i++;
	}
	return (NULL);
}
